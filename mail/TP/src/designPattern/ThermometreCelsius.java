package designPattern;

public class ThermometreCelsius {
	
	double temperatureEnCelsius;

	public ThermometreCelsius() {
	}

	public double getTemperature() {
		return this.temperatureEnCelsius;
	}

	public void setTemperature(double temperatureEnCelsius) {
		this.temperatureEnCelsius = temperatureEnCelsius;
	}

}
